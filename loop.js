// Write a program using simple for loop.
for (let i = 0; i <= 10; i++) {
  console.log(i);
}
// Write a program using for-of loop.
const numbers = [10, 20, 30, 40, 50];
for (number of numbers) {
  console.log(number);
}
// Write a program using for-in loop.
const myObj = {
  name: "raihan",
  age: 22,
  address: "mirpur-10",
};
for (let key in myObj) {
  console.log(key, myObj[key]);
}
